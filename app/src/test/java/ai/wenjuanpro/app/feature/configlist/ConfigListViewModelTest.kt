            sessionState = session,
            ioDispatcher = dispatcher,
        )

    private fun drainEffects(vm: ConfigListViewModel): List<ConfigListEffect> {
        val out = mutableListOf<ConfigListEffect>()
        val field =
            ConfigListViewModel::class.java.getDeclaredField("_effect").apply { isAccessible = true }
        @Suppress("UNCHECKED_CAST")
        val ch = field.get(vm) as Channel<ConfigListEffect>
        while (true) {
            val r = ch.tryReceive()
            if (r.isSuccess) out += r.getOrNull()!! else break
        }
        return out
    }

