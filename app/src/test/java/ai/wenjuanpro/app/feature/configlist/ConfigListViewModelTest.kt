    private fun invalidConfig(
        fileName: String = "broken.txt",
    ): ConfigLoadResult.Invalid =
        ConfigLoadResult.Invalid(
            fileName = fileName,
            errors =
                listOf(
                    ParseError(
                        line = 2,
